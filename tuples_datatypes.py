#Tuples

#Printing a tuple
my_tuple = (1, 2, 3, 4, 'Mithilesh', 'Alekhya')
print(my_tuple)

#printing by using index value
print(my_tuple[4])

#count how many 2 are there in the tuple
my_count = my_tuple.count(2)
print(my_count)

#index tells in which position 'Alekhya is there
my_index = my_tuple.index('Alekhya')
print(my_index)